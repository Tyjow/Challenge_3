function show() {
	document.getElementById('bloc-mail').style.left = '181px';
	document.getElementById('bloc-mail').style.opacity = 1;
	document.getElementById('bloc-mail').style.visibility = 'visible';
}

function hide() {
	document.getElementById('bloc-mail').style.left = '-300px';
	document.getElementById('bloc-mail').style.opacity = 0;
	document.getElementById('bloc-mail').style.visibility = 'hidden';
}

